	function __construct($code)
		{
		$code = strtoupper($code);
		if(Location::is_country_code($code))
			{
			$this->type = 'country';
			$this->code = $code;
			$this->name = Location::$iso_countries[$code];
			}
		elseif(Location::is_region_code($code))
			{
			$this->type = 'region';
			$this->code = $code;
			$this->name = Location::$iso_regions[$code];
			}
		}
	
	function regions($nonprefix = false)
		{
		if($this->type == 'country')
			{
			$regions = array();
			foreach(Location::$iso_regions as $r_code => $r_name)
				{
				if(preg_match("/^{$this->code}-(.*)$/", $r_code, $m))
					{
					if($nonprefix !== false)
						{
						$regions[$m[1]] = $r_name;
						}
					else
						{
						$regions[$r_code] = $r_name;
						}
					}
				}
			asort($regions);
			return $regions;
			}
		}

	static function from_code($code)
		{
		$l = new Location($code);
		return ( ($l->type == NULL) ? false : $l);
		}

	static function is_country_code($code)
		{
		return (isset(Location::$iso_countries[$code])) ? true : false;
		}

	static function is_region_code($code)
		{
		return (isset(Location::$iso_regions[$code])) ? true : false;
		}

	static function country_codes()
		{
		return array_keys(Location::$iso_countries);
		}

	static function region_codes()
		{
		return array_keys(Location::$iso_countries);
		}
